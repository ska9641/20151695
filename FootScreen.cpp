void FootScreen::TimeAttack()
{
	if (time->DelayCheck())
	{
		StageTime++;
	}
}

void FootScreen::Goal()
{
	StageCount++;
}

bool FootScreen::Failed()
{
	if (Stageinfo == StageTime)
	{
		return true;
	}
}

bool FootScreen::Clear()
{
	if(StageGoal == StageCount)
		return true;
}
